export function localizeSkill(skill, locale) {
  return {
    ...skill,
    name: skill.name[locale] || skill.name.zh,
    description: skill.description[locale] || skill.description.zh,
    author: {
      ...skill.author,
      name: skill.author.name[locale] || skill.author.name.zh,
    },
  }
}

export function getSkills(locale) {
  return skills.map(s => localizeSkill(s, locale))
}

export function getSkillBySlug(slug, locale) {
  const skill = skills.find(s => s.slug === slug)
  return skill ? localizeSkill(skill, locale) : null
}
